//  Function to Add Two Numbers
function add(a, b) {
    return a + b;
}

// Example usage:
console.log(add(3, 4)); // Output: 7

// Function to Check if a Number is Even
function isEven(num) {
    return num % 2 === 0;
}

// Example usage:
console.log(isEven(4)); // Output: true
console.log(isEven(7)); // Output: false
